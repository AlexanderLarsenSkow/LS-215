/*
input: word, text
output: every occurance of hte word highlighted.

Rules:
	highlight hte word each time it appears.

Examples:
	'**HIGHLIGHTEDWORD**'.
	all caps and ** ** 

Data Structures:
	regex that is replacing. We have a callback function.

Algo:
	create a regex that is word boundaried with ig and flags.
	return a replacement of the word in the regex and take the word in teh callback
	return it upcased and ** **
*/

const highlight = word => `**${word.toUpperCase()}**`;

function searchWord(word, text) {
	const regex = new RegExp(`\\b${word}\\b`, 'gi');
	return text.replace(regex, highlight);
}

